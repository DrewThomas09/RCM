def _safe_stem(packet: DealAnalysisPacket) -> str:
    import re
    base = packet.run_id or packet.deal_id or "packet"
    return re.sub(r"[^A-Za-z0-9_.-]+", "_", str(base))


def _fmt_cell_num(v: Optional[float]) -> str:
    if v is None or v != v:
        return ""
    return f"{float(v):.4f}"


