# ── Constants ────────────────────────────────────────────────────────

_PRODUCT_VERSION = "1.0"


def _utcnow() -> datetime:
    return datetime.now(timezone.utc)


# ── Value formatting (mirrors the workbench / renderer conventions) ──

def _fmt_money(v: Optional[float]) -> str:
    if v is None or v != v:
        return "—"
    f = float(v)
    if abs(f) >= 1e9:
        return f"${f/1e9:.2f}B"
    if abs(f) >= 1e6:
        return f"${f/1e6:.1f}M"
    if abs(f) >= 1e3:
        return f"${f/1e3:,.0f}K"
    return f"${f:,.0f}"


def _fmt_pct(v: Optional[float]) -> str:
    if v is None or v != v:
        return "—"
    return f"{float(v):.1f}%"


def _fmt_num(v: Optional[float], dp: int = 2) -> str:
    if v is None or v != v:
        return "—"
    return f"{float(v):.{dp}f}"


def _esc(s: Any) -> str:
    return html.escape("" if s is None else str(s))


# ── Audit footer ─────────────────────────────────────────────────────

@dataclass
class AuditFooter:
    """Footer text used in every export (HTML/PPTX/DOCX/MD).

    Partners specifically asked for packet hash + run_id on every
    printed surface so a stale deck can be traced back to the exact
    analysis that produced it. The footer is generated once per
    export call and passed into each format's template.
    """
    run_id: str
    input_hash: str
    observed_count: int
    predicted_count: int
    generated_at: datetime
    model_version: str

    def as_lines(self) -> List[str]:
        ts = self.generated_at.strftime("%Y-%m-%d %H:%M UTC")
        return [
            f"Generated by RCM-MC v{_PRODUCT_VERSION} on {ts}",
            f"Analysis Packet ID: {self.run_id}",
            f"Input Hash: {self.input_hash or 'n/a'}",
            f"This analysis is based on {self.observed_count} observed "
            f"and {self.predicted_count} predicted metrics.",
            f"Model version: {self.model_version}",
        ]

    def as_html(self) -> str:
        return "<br>".join(html.escape(line) for line in self.as_lines())

    def as_plain_text(self) -> str:
        return "\n".join(self.as_lines())


def _footer_for(
    packet: DealAnalysisPacket,
    inputs_hash: str = "",
) -> AuditFooter:
    return AuditFooter(
        run_id=packet.run_id or "",
        input_hash=inputs_hash,
        observed_count=len(packet.observed_metrics or {}),
        predicted_count=len(packet.predicted_metrics or {}),
        generated_at=packet.generated_at or _utcnow(),
        model_version=packet.model_version or PACKET_SCHEMA_VERSION,
    )


# ── Renderer ─────────────────────────────────────────────────────────

class PacketRenderer:
    """Render a packet into HTML / PPTX / JSON / CSV / DOCX artifacts.

    Output files land under ``out_dir`` (defaults to a fresh temp dir so
    tests don't leak). Callers who want deterministic paths — the
    server for `/exports/downloads/...` URLs, the CLI for `--out` — pass
    their own directory.
    """

    def __init__(self, out_dir: Optional[Path] = None) -> None:
        if out_dir is None:
            out_dir = Path(tempfile.mkdtemp(prefix="rcm_exports_"))
        out_dir = Path(out_dir)
        out_dir.mkdir(parents=True, exist_ok=True)
        self.out_dir = out_dir

    # ── HTML diligence memo ─────────────────────────────────────

    def render_diligence_memo_html(
        self,
        packet: DealAnalysisPacket,
        *,
        inputs_hash: str = "",
    ) -> str:
        footer = _footer_for(packet, inputs_hash)
        sections = [
            _memo_section_exec_summary(packet),
            _memo_section_completeness(packet),
            _memo_section_rcm_performance(packet),
            _memo_section_bridge(packet),
            _memo_section_risks(packet),
            _memo_section_mc(packet),
            _memo_section_diligence_questions(packet),
            _memo_section_comparables(packet),
        ]
        return (
            '<!DOCTYPE html>'
            '<html lang="en"><head><meta charset="utf-8">'
            f'<title>{_esc(packet.deal_name or packet.deal_id)} · Diligence Memo</title>'
            f'<style>{_MEMO_CSS}</style>'
            '</head>'
            '<body><div class="memo-body">'
            f'<h1>{_esc(packet.deal_name or packet.deal_id)}</h1>'
            f'<p class="muted">Diligence Memo · {packet.generated_at.strftime("%Y-%m-%d")}</p>'
            + "".join(sections)
            + f'<footer class="memo-footer">{footer.as_html()}</footer>'
            '</div></body></html>'
        )

    # ── PPTX ────────────────────────────────────────────────────

    def render_diligence_memo_pptx(
        self,
        packet: DealAnalysisPacket,
        *,
        inputs_hash: str = "",
    ) -> Path:
        footer = _footer_for(packet, inputs_hash)
        try:
            from pptx import Presentation    # type: ignore
            from pptx.util import Inches, Pt  # type: ignore
        except ImportError:
            return self._render_pptx_fallback(packet, footer)

        prs = Presentation()
        prs.slide_width = Inches(13.333)
        prs.slide_height = Inches(7.5)

        # Slide 1: Title
        slide = prs.slides.add_slide(prs.slide_layouts[0])
        slide.shapes.title.text = (
            f"RCM Diligence Memo — {packet.deal_name or packet.deal_id}"
        )
        slide.placeholders[1].text = (
            f"{packet.generated_at.strftime('%Y-%m-%d')}\n"
            f"Analysis ID: {packet.run_id}"
        )

        # Slide 2: Executive summary + completeness
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        slide.shapes.title.text = "Executive Summary"
        body = slide.placeholders[1]
        body.text = _memo_exec_text(packet)

        # Slide 3: RCM profile (key metrics as text list — text is the
        # most compatible thing across python-pptx versions; tables are
        # fiddly when we can't assume a template layout).
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        slide.shapes.title.text = "RCM Profile"
        slide.placeholders[1].text = _memo_profile_text(packet)

        # Slide 4: Bridge
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        slide.shapes.title.text = "EBITDA Value Creation"
        slide.placeholders[1].text = _memo_bridge_text(packet)

        # Slide 5: Monte Carlo
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        slide.shapes.title.text = "Monte Carlo Returns"
        slide.placeholders[1].text = _memo_mc_text(packet)

        # Slide 6: Risks
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        slide.shapes.title.text = "Risk Flags"
        slide.placeholders[1].text = _memo_risks_text(packet)

        # Slide 7: Diligence questions
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        slide.shapes.title.text = "Diligence Questions (P0)"
        slide.placeholders[1].text = _memo_questions_text(packet)

        # Slide 8: Comparables + footer
        slide = prs.slides.add_slide(prs.slide_layouts[1])
        slide.shapes.title.text = "Comparable Set & Audit"
        slide.placeholders[1].text = (
            _memo_comparables_text(packet)
            + "\n\n"
            + footer.as_plain_text()
        )

        path = self.out_dir / f"{_safe_stem(packet)}.pptx"
        prs.save(str(path))
        return path

    def _render_pptx_fallback(
        self, packet: DealAnalysisPacket, footer: AuditFooter,
    ) -> Path:
        """Hand-built minimal OOXML ``.pptx`` via stdlib ``zipfile``.

        When ``python-pptx`` is absent we still produce a file partners
        can open in PowerPoint. It isn't beautiful — no theme images,
        no master art — but the deck has the eight required slides
        (title + bullets), valid relationships, and valid content
        types, which is enough for Office + LibreOffice to load it.

        Kept in this module (rather than a separate ``pptx_fallback``
        file) so the public surface stays narrow: the server and CLI
        just call ``render_diligence_memo_pptx`` and get back a
        ``.pptx`` path regardless of whether ``python-pptx`` is
        installed. Tests assert both outputs have the same set of
        slide IDs.
        """
        slides_content: List[Tuple[str, str]] = [
            ("Title",
                f"{packet.deal_name or packet.deal_id}\n"
                f"Generated {packet.generated_at.strftime('%Y-%m-%d')}"),
            ("Executive Summary", _memo_exec_text(packet)),
            ("RCM Profile", _memo_profile_text(packet)),
            ("EBITDA Value Creation", _memo_bridge_text(packet)),
            ("Monte Carlo Returns", _memo_mc_text(packet)),
            ("Risk Flags", _memo_risks_text(packet)),
            ("Diligence Questions (P0)", _memo_questions_text(packet)),
            ("Comparables & Audit",
                _memo_comparables_text(packet)
                + "\n\n" + footer.as_plain_text()),
        ]
        path = self.out_dir / f"{_safe_stem(packet)}.pptx"
        _write_minimal_pptx(path, slides_content)
        return path

    # ── XLSX ────────────────────────────────────────────────────

    def render_deal_xlsx(
        self,
        packet: DealAnalysisPacket,
        *,
        inputs_hash: str = "",
    ) -> Path:
        """Multi-sheet Excel workbook. Falls back to CSV when
        ``openpyxl`` is missing.

        Partners want Excel because downstream workflows (decks, LP
        updates, IC memos) all live there. The CSV export stays as
        the stdlib-only fallback; ``render_deal_xlsx`` is the
        structured, multi-sheet version when the optional dep is
        present.
        """
        from .xlsx_renderer import render_deal_xlsx as _render_xlsx
        try:
            return _render_xlsx(
                packet, self.out_dir, inputs_hash=inputs_hash,
            )
        except ImportError:
            # openpyxl absent — fall back to the CSV export the caller
            # was going to get anyway. The audit row records ``csv``
            # so partners see the fallback happened.
            return self.render_raw_data_csv(
                packet, inputs_hash=inputs_hash,
            )

