"""Packet-driven export renderer.

Every export path — HTML memo, PPTX deck, JSON blob, raw CSV, DOCX
questions, LP update — goes through :class:`PacketRenderer`. Each
method takes a :class:`DealAnalysisPacket` (or a list of them for the
portfolio LP update) and returns a rendered artifact.

Why this module exists:
- Partners kept finding different numbers in the PPTX vs. the live
  UI because each export pulled from the store independently. With
  the packet as the single source of truth, numbers can't drift.
- Audit. Every export footer prints the packet hash and run_id, so a
  partner who sees a stale number in a printed deck can pinpoint the
  exact analysis run that produced it.

Optional dependencies:
- ``python-pptx`` — if missing, the PPTX path emits a structured
  fallback .txt with the same outline, so callers never get a silent
  None. We've had partners paste from the fallback into an external
  PPT; that's a valid workflow.
- ``python-docx`` — same pattern, fallback is a partner-ready markdown
  file that opens cleanly in Word / Google Docs.
"""
