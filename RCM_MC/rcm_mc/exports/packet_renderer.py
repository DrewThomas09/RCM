def _write_minimal_pptx(
    path: Path, slides: List[Tuple[str, str]],
) -> None:
    """Emit a minimum-viable OOXML ``.pptx`` zip archive.

    Parts we produce (matches what PowerPoint and LibreOffice accept
    as a no-theme deck):

    - ``[Content_Types].xml`` — content-type mappings for every part.
    - ``_rels/.rels`` — root → ``ppt/presentation.xml``.
    - ``ppt/presentation.xml`` — slide size (720×540 pt), slide ID
      list referencing the slide rels.
    - ``ppt/_rels/presentation.xml.rels`` — rels to each slide, the
      slide master, and the theme.
    - ``ppt/slideMasters/slideMaster1.xml`` + rels.
    - ``ppt/slideLayouts/slideLayout1.xml`` + rels.
    - ``ppt/theme/theme1.xml`` — bare ``a:theme`` element (no colors
      beyond the defaults); PowerPoint fills in the rest.
    - ``ppt/slides/slideN.xml`` + rels for each slide.

    Keeping the XML inline as constants here rather than as data files
    because this fallback needs to work from a fresh stdlib-only
    install — if we shipped template files under ``package-data``
    someone skipping that data dir (e.g. pyinstaller) would break the
    fallback silently.
    """
    import zipfile
    from html import escape as _xml_escape

    def _text_body(text: str) -> str:
        """Split ``text`` into paragraphs → ``<a:p>`` bullet blocks."""
        paragraphs: List[str] = []
        for raw_line in (text or "").split("\n"):
            line = raw_line.rstrip()
            if not line:
                paragraphs.append('<a:p><a:endParaRPr lang="en-US"/></a:p>')
                continue
            paragraphs.append(
                '<a:p><a:r><a:rPr lang="en-US" dirty="0"/>'
                f'<a:t>{_xml_escape(line)}</a:t></a:r></a:p>'
            )
        return "".join(paragraphs) or (
            '<a:p><a:endParaRPr lang="en-US"/></a:p>'
        )

