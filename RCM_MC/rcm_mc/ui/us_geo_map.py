#{cid} .usgeo-state[data-clickable='1']:focus-visible{{outline:none;stroke:var(--sc-teal,#1f7a5a);stroke-width:2;}}
#{cid} .usgeo-state.usgeo-accent{{stroke:{ac};stroke-width:1.5;}}
#{cid} .usgeo-state.usgeo-selected{{stroke:var(--sc-navy,#0b2341);stroke-width:2.4;}}
