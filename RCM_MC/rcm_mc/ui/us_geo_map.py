"""Real geographic US state map (SVG paths) for the Portfolio Map.

Drop-in replacement for the old tile-grid cartogram (`render_us_state_map`):
same call signature, but renders the actual contiguous-US geography from
vendored Albers-projected state paths (`_us_geo_paths.py`, generated by
`tools/build_us_geo_paths.py` from the public-domain US Census GeoJSON).
Alaska & Hawaii are scaled bottom-left insets. 100% local — no map tiles, no
GeoJSON fetch, no external APIs at runtime.

States shade on a cream→green scale by value; Certificate-of-Need (accent)
states carry an amber outline; the selected state a navy outline. Click or
keyboard-activate a state to dispatch a ``us-map-select`` CustomEvent (same
event the old cartogram emitted) so existing page wiring keeps working.
"""
from __future__ import annotations

import html as _html
from typing import Callable, Dict, List, Optional

from ._us_geo_paths import US_GEO_VIEWBOX, US_STATE_PATHS

_NODATA = "#e7dfca"      # muted cream — state with no value
_G_LO = (214, 232, 223)  # light green
_G_HI = (24, 87, 63)     # green-deep
_ACCENT = "#b8842e"      # amber — CON / accent jurisdictions
_uid = [0]


def _esc(s) -> str:
    return _html.escape("" if s is None else str(s))


def _shade(value: Optional[float], lo: float, hi: float) -> str:
    if value is None:
        return _NODATA
    if hi <= lo:
        t = 1.0
    else:
        t = max(0.0, min(1.0, (value - lo) / (hi - lo)))
    r = round(_G_LO[0] + (_G_HI[0] - _G_LO[0]) * t)
    g = round(_G_LO[1] + (_G_HI[1] - _G_LO[1]) * t)
    b = round(_G_LO[2] + (_G_HI[2] - _G_LO[2]) * t)
    return f"rgb({r},{g},{b})"


