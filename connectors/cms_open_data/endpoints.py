ENDPOINTS: Dict[str, EndpointSpec] = {
    _CATALOG_SPEC.key: _CATALOG_SPEC,
    **_CURATED,
    _GENERIC_SPEC.key: _GENERIC_SPEC,
}


def get_endpoint(key: str) -> EndpointSpec:
    """Look up an endpoint spec by key, raising a clear error if absent."""
    try:
        return ENDPOINTS[key]
    except KeyError as exc:
        raise KeyError(
            f"unknown CMS Open Data endpoint {key!r}; known: {sorted(ENDPOINTS)}"
        ) from exc


def find_endpoint(dataset: str) -> "EndpointSpec | None":
    """Resolve a spec from either its short key or its full dataset_id."""
    if dataset in ENDPOINTS:
        return ENDPOINTS[dataset]
    prefix = "cms_open_data_"
    if dataset.startswith(prefix) and dataset[len(prefix):] in ENDPOINTS:
        return ENDPOINTS[dataset[len(prefix):]]
    return None


def curated_endpoints() -> List[EndpointSpec]:
    return [s for s in ENDPOINTS.values() if s.kind == "curated"]
