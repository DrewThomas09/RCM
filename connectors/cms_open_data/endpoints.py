"""Declarative specs for every CMS Open Data dataset this connector ingests.

One :class:`EndpointSpec` per dataset. Three kinds:

  ``catalog``  — the DCAT catalog itself (``GET /data.json``); one row per
                 dataset data.cms.gov publishes (158 at snapshot time).
  ``curated``  — flagship datasets landed in first-class canonical tables
                 whose columns were snapshotted from LIVE API samples
                 (``size=5`` probes, 2026-07-06) and snake_cased with the
                 deterministic :func:`connectors.cms_open_data.normalize._snake`.
  ``generic``  — the on-demand row store (``cms_open_data_rows``) through
                 which *any* catalog dataset can be pulled without new code.

Why specs and not code branches: adding or retuning a dataset is a data
edit here, never new routing logic elsewhere. The registry rows
(:mod:`connectors.cms_open_data.registry`), the canonical tables
(:mod:`connectors.cms_open_data.tables`) and the connector all read these.

UUID pinning + re-resolution
----------------------------
The data API addresses a dataset by the UUID of its *latest version*, and
that UUID rotates when CMS publishes a new data year. Each curated spec
pins the UUID that was live at snapshot time **and** carries the dataset's
exact catalog ``title``: at fetch time the connector prefers the UUID
re-resolved from a synced catalog table (matched by title slug) and falls
back to the pinned literal, so a stale pin degrades gracefully instead of
breaking (see ``CmsOpenDataConnector.resolve_uuid``).

``natural_key`` lists ORIGINAL API column names (the upsert key is
composed from their values in normalize.py); ``join_keys``/``columns``
are their snake_cased forms as stored in SQLite.
"""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import Dict, List, Tuple

_BASE_URL = "https://data.cms.gov"

CATALOG_TABLE = "cms_open_data_catalog"
GENERIC_TABLE = "cms_open_data_rows"


@dataclass(frozen=True)
class EndpointSpec:
    """One CMS Open Data dataset.

    Attributes
    ----------
    key:
        Short dataset id; ``cms_open_data_{key}`` is the estate-wide
        dataset id and (for curated datasets) the canonical table suffix.
        Also the value written into ``source_endpoint`` for slice pinning.
    kind:
        ``catalog`` | ``curated`` | ``generic`` — drives which normalizer
        runs and which table the rows land in.
    uuid:
        The latest-version dataset UUID pinned at snapshot time (empty
        for the catalog/generic pseudo-datasets). May rotate — see the
        module docstring.
    title:
        The dataset's exact ``data.json`` title; the stable handle used
        to re-resolve the current UUID from a synced catalog.
    natural_key:
        ORIGINAL API column names whose values compose the idempotent
        upsert key (built in normalize.py as ``{key}:{v1}:{v2}…``).
    join_keys:
        The natural-key columns snake_cased — the estate-level join hints.
    columns:
        Every data column, snake_cased, in live API order (excludes the
        pk and the ``(source_endpoint, ingested_at)`` meta columns).
    date_field:
        snake_cased column usable for recency sorting ("" when none).
    """

    key: str
    kind: str
    uuid: str
    title: str
    natural_key: Tuple[str, ...]
    join_keys: Tuple[str, ...]
    columns: Tuple[str, ...]
    date_field: str = ""
    refresh_cadence: str = "annual"
    default_params: Dict[str, str] = field(default_factory=dict)

    @property
    def dataset_id(self) -> str:
        return f"cms_open_data_{self.key}"

    @property
    def target_table(self) -> str:
        if self.kind == "catalog":
            return CATALOG_TABLE
        if self.kind == "generic":
            return GENERIC_TABLE
        return f"cms_open_data_{self.key}"

    @property
    def pk(self) -> str:
        # The catalog is keyed by the title slug; everything else by the
        # composed natural key (or dataset_key:row_idx for generic rows).
        return "dataset_key" if self.kind == "catalog" else "row_key"

    @property
    def path(self) -> str:
        if self.kind == "catalog":
            return "/data.json"
        if self.kind == "generic":
            return "/data-api/v1/dataset/{uuid}/data"   # template, uuid at call time
        return f"/data-api/v1/dataset/{self.uuid}/data"

    @property
    def base_url(self) -> str:
        return _BASE_URL


